import turtle 
turtle.Screen().setup(300,500)
turtle.Screen().bgcolor("pink")
turtle.Screen().title("star")
t=turtle.Turtle ()
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)
t.penup()
t.goto(0,60)
t.pendown()
t.left(120)
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
turtle.done
